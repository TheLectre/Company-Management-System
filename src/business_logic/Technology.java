package business_logic;

public class Technology {

    //================================================================================
    // Fields
    //================================================================================
    private int id;
    private String name;

    //================================================================================
    // Constructors
    //================================================================================
    public Technology(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Technology(String name) {
        this.name = name;
    }
    

    //================================================================================
    // Methods
    //================================================================================
    
    
    //================================================================================
    // Accessors
    //================================================================================
    public int getID() {
        return id;
    }

    public String getName() {
        return name;
    }
}
